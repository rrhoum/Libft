#include <libft.h>

char		*ft_strrchr(const char *s, int c)
{
	size_t	i;
	char	*a;

	i = 0;
	a = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			a = (char *)&s[i];
		i++;
	}
	if (a == 0)
		return (0);
	else
		return (a);
}
