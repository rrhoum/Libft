char		*ft_strrchr(const char *s, int c)
{
	size_t	i;
	char	*s1;
	char	*a;

	i = 0;
	s1 = s;
	a = 0;
	while (s1[i] != '\0')
	{
		if (s1[i] == c)
			a = &s1[i];
		i++;
	}
	return (a);
}
